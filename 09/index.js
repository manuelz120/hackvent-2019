const CellularAutomata = require("cellular-automata");
const { flatten } = require("lodash");
const sha256File = require("sha256-file");
const Jimp = require("jimp");

const qrDecoder = require("node-zxing")();

async function performEvolutionWithRule(data, rule) {
  const fileCache = {};
  const cellularAutomata = new CellularAutomata([33, 33]);
  cellularAutomata.setRule(`W${rule}`);
  cellularAutomata.array.data = flatten(data);

  cellularAutomata.iterate(1);

  let dataValid = false;
  while (!dataValid) {
    const image = await Jimp.create(33, 33);

    for (let y = 0; y < 33; y++) {
      for (let x = 0; x < 33; x++) {
        const item = cellularAutomata.array.data[y * 33 + x];
        image.setPixelColor(!item ? 0xffffffff : 0xff000000, x, y);
      }
    }

    await image.writeAsync("./flag.jpg");

    const hash = sha256File("./flag.jpg");

    const [error, code] = await new Promise(resolve =>
      qrDecoder.decode("./flag.jpg", (error, code) => resolve([error, code]))
    );

    dataValid = code.length > 0;
    if (dataValid) {
      console.log(error, code);
    }

    cellularAutomata.iterate(1);

    if (!fileCache[hash]) {
      fileCache[hash] = 1;
    } else {
      console.log("Done");
      return false;
    }
  }

  return true;
}

